package advanced;

import java.util.LinkedHashSet;

public class LinkedHashSetEx1 {

	public static void main(String[] args) {
		
		
		LinkedHashSet  lhs = new LinkedHashSet();
		lhs.add("Hello Java");
		lhs.add(1002);
		lhs.add(true);
		lhs.add(2035);
		
		System.out.println("The Values of lhs is : "+lhs);
		
	}

}
